public class Student {
    public String Name;
    public String Notes;
    public String LastSeen;
    public String URL;

    public Student(String Name, String Notes, String LastSeen, String URL){
        this.Name = Name;
        this.Notes = Notes;
        this.LastSeen = LastSeen;
        this.URL = URL;
    }

}
